public class Casting {
    public static void main(String[] args){
     double price=100.18;
     double finalPrice=price+18;  //implicit Casting->compile will do automatically
     System.out.println(finalPrice);                 
              
     int p=100;
     int fp=p+(int)18.9;   //explicit casting->here programmer will do itself
     System.out.println(fp);
    }    
}












